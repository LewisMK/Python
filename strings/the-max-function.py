# the max() function finds the maximum element of the sequence passed as an argument
# this sequence cannot be empty. We get a ValueError exception

# Example 1

print(max("aAbByYzZ"))

# Example 2

t = "Lewis said 'Hey there!'"

print('[' + max(t) + ']')

# Example 3

t = [0, 1, 2]
print(max(t))

