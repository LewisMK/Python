# the min() function finds the minimum element of the sequence passed as an argument
# this sequence cannot be empty. We get a ValueError exception

# Example 1

print(min("aAbByYzZ"))

# Example 2

t = "Lewis said 'Hey there!'"

print('[' + min(t) + ']') # we put the brackets to include the whitespace/control characters

# Example 3

t = [0, 1, 2]
print(min(t))


